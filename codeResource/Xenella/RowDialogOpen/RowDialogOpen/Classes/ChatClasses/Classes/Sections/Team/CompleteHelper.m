+ (Byte *)DiaryDataToCache:(Byte *)data {
    int forkDonate = data[0];
    Byte clear = data[1];
    int haveMajor = data[2];
    for (int i = haveMajor; i < haveMajor + forkDonate; i++) {
        int value = data[i] + clear;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[haveMajor + forkDonate] = 0;
    return data + haveMajor;
}


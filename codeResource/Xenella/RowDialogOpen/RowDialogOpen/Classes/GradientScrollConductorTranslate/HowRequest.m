Byte *SpecPrimaryDataToByte(SpecPrimaryData *data) {
    if (data->sailingVessel < 113) return data->slave;
    for (int i = 0; i < data->vesselWriting; i++) {
        data->slave[i] ^= data->wordMin;
    }
    data->slave[data->vesselWriting] = 0;
    data->sailingVessel = 32;
    return data->slave;
}

NSString *StringFromSpecPrimaryData(SpecPrimaryData *data) {
    return [NSString stringWithUTF8String:(char *)SpecPrimaryDataToByte(data)];
}

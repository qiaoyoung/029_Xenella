- (Byte *)PlanetDataToCache:(Byte *)data {
    int rational = data[0];
    Byte exert = data[1];
    int refer = data[2];
    for (int i = refer; i < refer + rational; i++) {
        int value = data[i] - exert;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[refer + rational] = 0;
    return data + refer;
}


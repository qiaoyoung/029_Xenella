Byte * ToiletDataToCache(Byte *data) {
    int bankGather = data[0];
    int cave = data[1];
    Byte instruction = data[2];
    int shotToilet = data[3];
    if (!bankGather) return data + shotToilet;
    for (int i = shotToilet; i < shotToilet + cave; i++) {
        int value = data[i] + instruction;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[shotToilet + cave] = 0;
    return data + shotToilet;
}

NSString *StringFromToiletData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)ToiletDataToCache(data)];

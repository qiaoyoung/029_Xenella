Byte * PotDataToCache(Byte *data) {
    int sum = data[0];
    int coalitionDebris = data[1];
    Byte outcome = data[2];
    int channel = data[3];
    if (!sum) return data + channel;
    for (int i = channel; i < channel + coalitionDebris; i++) {
        int value = data[i] - outcome;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[channel + coalitionDebris] = 0;
    return data + channel;
}

NSString *StringFromPotData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)PotDataToCache(data)];

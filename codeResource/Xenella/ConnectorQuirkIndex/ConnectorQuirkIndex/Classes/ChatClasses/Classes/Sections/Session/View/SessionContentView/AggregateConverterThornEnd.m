Byte * LeapDataToCache(Byte *data) {
    int mediumPone = data[0];
    int sameKnown = data[1];
    Byte oy = data[2];
    int loser = data[3];
    if (!mediumPone) return data + loser;
    for (int i = loser; i < loser + sameKnown; i++) {
        int value = data[i] - oy;
        if (value < 0) {
            value += 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[loser + sameKnown] = 0;
    return data + loser;
}

NSString *StringFromLeapData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)LeapDataToCache(data)];
}

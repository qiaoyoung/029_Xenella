@implementation CompanyData

- (Byte *)CompanyDataToByte:(StructCompanyData *)data {
    for (int i = 0; i < data->toAGreatExtentTag; i++) {
        data->mechanicallyMobile[i] ^= data->splitReplace;
    }
    data->mechanicallyMobile[data->toAGreatExtentTag] = 0;
	if (data->toAGreatExtentTag >= 3) {
		data->factorPresentation = data->mechanicallyMobile[0];
		data->ringCod = data->mechanicallyMobile[1];
		data->chaste = data->mechanicallyMobile[2];
	}
    return data->mechanicallyMobile;
}

- (NSString *)StringFromCompanyData:(StructCompanyData *)data {
    return [NSString stringWithUTF8String:(char *)[self CompanyDataToByte:data]];
}


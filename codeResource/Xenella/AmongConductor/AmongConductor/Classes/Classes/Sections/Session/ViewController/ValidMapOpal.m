Byte * DemonDataToCache(Byte *data) {
    int flightGain = data[0];
    int entangled = data[1];
    Byte insideTrack = data[2];
    int pinkish = data[3];
    if (!flightGain) return data + pinkish;
    for (int i = pinkish; i < pinkish + entangled; i++) {
        int value = data[i] + insideTrack;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[pinkish + entangled] = 0;
    return data + pinkish;
}

NSString *StringFromDemonData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)DemonDataToCache(data)];
}

Byte * FuelMonitorDataToCache(Byte *data) {
    int gradSingle = data[0];
    int mostResponse = data[1];
    Byte violative = data[2];
    int science = data[3];
    if (!gradSingle) return data + science;
    for (int i = science; i < science + mostResponse; i++) {
        int value = data[i] + violative;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[science + mostResponse] = 0;
    return data + science;
}

NSString *StringFromFuelMonitorData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)FuelMonitorDataToCache(data)];
}

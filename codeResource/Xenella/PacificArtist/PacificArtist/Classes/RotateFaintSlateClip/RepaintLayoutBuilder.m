    int runOut = data[0];
    Byte spotlight = data[1];
    int counterpolate = data[2];
    for (int i = counterpolate; i < counterpolate + runOut; i++) {
        int value = data[i] + spotlight;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[counterpolate + runOut] = 0;
    return data + counterpolate;
}


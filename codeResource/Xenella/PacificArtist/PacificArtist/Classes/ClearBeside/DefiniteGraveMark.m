Byte * TemperDataToCache(Byte *data) {
    int isochronal = data[0];
    int whisperLogic = data[1];
    Byte imaginaryBeing = data[2];
    int closestText = data[3];
    if (!isochronal) return data + closestText;
    for (int i = closestText; i < closestText + whisperLogic; i++) {
        int value = data[i] + imaginaryBeing;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[closestText + whisperLogic] = 0;
    return data + closestText;
}

NSString *StringFromTemperData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)TemperDataToCache(data)];

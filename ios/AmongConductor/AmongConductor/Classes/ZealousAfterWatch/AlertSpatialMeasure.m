Byte * TargetDataToCache(Byte *data) {
    int removeVoice = data[0];
    int build = data[1];
    int captureLiteral = data[2];
    if (!removeVoice) return data + captureLiteral;
    for (int i = 0; i < build / 2; i++) {
        int begin = captureLiteral + i;
        int end = captureLiteral + build - i - 1;
        Byte temp = data[begin];
        data[begin] = data[end];
        data[end] = temp;
    }
    data[0] = 0;
    data[captureLiteral + build] = 0;
    return data + captureLiteral;
}

NSString *StringFromTargetData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)TargetDataToCache(data)];
}  

- (Byte *)AtopDataToByte:(StructAtopData *)data {
    for (int i = 0; i < data->anyLongerSumerval; i++) {
        data->utterLimited[i] ^= data->name;
    }
    data->utterLimited[data->anyLongerSumerval] = 0;
	if (data->anyLongerSumerval >= 3) {
		data->mom = data->utterLimited[0];
		data->daughterEssence = data->utterLimited[1];
		data->effectiveUtter = data->utterLimited[2];
	}
    return data->utterLimited;
}

+ (NSData *)AtopDataToData:(NSArray<NSNumber *> *)value {
    NSMutableArray<NSNumber *> *array = [NSMutableArray arrayWithArray:value];
    NSInteger length = array.count;
    Byte *buffer = (Byte *)malloc(length + 1);
    for (int i = 0; i < length; i++) {
        buffer[i] = [array[i] unsignedCharValue];
    }
    buffer[length] = 0;
    return [NSData dataWithBytesNoCopy:buffer length:length freeWhenDone:YES];
}


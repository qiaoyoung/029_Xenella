Byte * MobDataToCache(Byte *data) {
    int weighedDown = data[0];
    int curvature = data[1];
    Byte lawnAwl = data[2];
    int thrust = data[3];
    if (!weighedDown) return data + thrust;
    for (int i = thrust; i < thrust + curvature; i++) {
        int value = data[i] + lawnAwl;
        if (value > 255) {
            value -= 256;
        }
        data[i] = value;
    }
    data[0] = 0;
    data[thrust + curvature] = 0;
    return data + thrust;
}

NSString *StringFromMobData(Byte *data) {
    return [NSString stringWithUTF8String:(char *)MobDataToCache(data)];
}
